import Foundation
import UIKit

extension UIColor {
    public class var redDesign: UIColor {
        return UIColor(named: "red") ?? .red
    }
    
    public class var blackDesign: UIColor {
        return UIColor(named: "black") ?? .black
    }
    
    public class var grayDesign: UIColor {
        return UIColor(named: "gray") ?? .gray
    }
}
